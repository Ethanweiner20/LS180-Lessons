/* Levels of Schema */

/*

1. Conceptual, logical, physical
2. Conceptual schema: A schema that simply represents the entities and
their relationships, without diving into database specifics
- Usually uses chicken-foot labelling to diagram relationships
3. Physical schema: Low-level design focused on how relationships between
entities are translated to actual tables and connections between those tables
via PK-FK references.
4. One-to-one, one-to-many, many-to-many

*/